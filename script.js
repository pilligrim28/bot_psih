// Рендер вопроса
function renderQuestion() {
    const question = QUESTIONS[currentQuestion];
    const selectedKey = answers[currentQuestion];

    // Формируем текст с вариантами ответов
    const optionsText = Object.entries(question.options).map(([key, value]) => {
        const isSelected = selectedKey === key;
        const icon = isSelected ? '✅ ' : `<b>${key}:</b> `;
        return `<div class="option-text ${isSelected ? 'selected' : ''}">${icon}${value}</div>`;
    }).join('');

    questionCard.innerHTML = `
        <h2 class="question-text">${question.text}</h2>
        <div class="options-display">
            ${optionsText}
        </div>
        <div class="answer-buttons">
            ${Object.keys(question.options).map(key => `
                <button class="answer-btn ${selectedKey === key ? 'active' : ''}" data-key="${key}">
                    ${selectedKey === key ? '✅ ' : ''}${key}
                </button>
            `).join('')}
        </div>
    `;

    // Добавляем обработчики кликов на кнопки
    document.querySelectorAll('.answer-btn').forEach(btn => {
        btn.addEventListener('click', () => selectOption(btn.dataset.key));
    });

    // Анимация появления
    questionCard.style.animation = 'none';
    setTimeout(() => {
        questionCard.style.animation = 'fadeIn 0.4s ease-out';
    }, 10);
}

// Выбор ответа
function selectOption(key) {
    answers[currentQuestion] = key;
    
    // Обновляем кнопки
    document.querySelectorAll('.answer-btn').forEach(btn => {
        const isActive = btn.dataset.key === key;
        btn.classList.toggle('active', isActive);
        btn.innerHTML = isActive ? `✅ ${btn.dataset.key}` : btn.dataset.key;
    });
    
    // Обновляем текст вариантов
    document.querySelectorAll('.option-text').forEach((textDiv, index) => {
        const keys = Object.keys(QUESTIONS[currentQuestion].options);
        const optionKey = keys[index];
        const isSelected = optionKey === key;
        textDiv.classList.toggle('selected', isSelected);
        textDiv.innerHTML = isSelected ? `✅ ${QUESTIONS[currentQuestion].options[optionKey]}` : `<b>${optionKey}:</b> ${QUESTIONS[currentQuestion].options[optionKey]}`;
    });

    updateButtons();

    // Автопереход на следующий вопрос через небольшую задержку
    if (currentQuestion < QUESTIONS.length - 1) {
        setTimeout(() => {
            nextQuestion();
        }, 400);
    } else {
        // Последний вопрос — показываем кнопку "Показать результат"
        btnNext.innerHTML = `
            Показать результат
            <svg width="20" height="20" viewBox="0 0 20 20" fill="none">
                <path d="M10 15l5-5-5-5v10z" fill="currentColor"/>
            </svg>
        `;
        btnNext.disabled = false;
    }
}

// Переход к следующему вопросу
function nextQuestion() {
    if (currentQuestion < QUESTIONS.length - 1) {
        currentQuestion++;
        renderQuestion();
        updateProgress();
        updateButtons();
    } else {
        // Показываем результат
        showResult();
    }
}

// Переход к предыдущему вопросу
function prevQuestion() {
    if (currentQuestion > 0) {
        currentQuestion--;
        renderQuestion();
        updateProgress();
        updateButtons();
        
        // Восстанавливаем текст кнопки
        btnNext.innerHTML = `
            Далее
            <svg width="20" height="20" viewBox="0 0 20 20" fill="none">
                <path d="M7.5 15L12.5 10L7.5 5" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"/>
            </svg>
        `;
    }
}

// Обновление прогресса
function updateProgress() {
    const progress = (currentQuestion / QUESTIONS.length) * 100;
    progressFill.style.width = `${progress}%`;
    currentQuestionSpan.textContent = currentQuestion + 1;
    progressPercent.textContent = `${Math.round(progress)}%`;
}

// Обновление кнопок
function updateButtons() {
    btnPrev.disabled = currentQuestion === 0;
    
    const hasAnswer = answers[currentQuestion] !== undefined;
    
    // Если последний вопрос и есть ответ — меняем текст кнопки
    if (currentQuestion === QUESTIONS.length - 1 && hasAnswer) {
        btnNext.innerHTML = `
            Показать результат
            <svg width="20" height="20" viewBox="0 0 20 20" fill="none">
                <path d="M10 15l5-5-5-5v10z" fill="currentColor"/>
            </svg>
        `;
        btnNext.disabled = false;
    } else {
        btnNext.innerHTML = `
            Далее
            <svg width="20" height="20" viewBox="0 0 20 20" fill="none">
                <path d="M7.5 15L12.5 10L7.5 5" stroke="currentColor" stroke-width="2" stroke-linecap="round" stroke-linejoin="round"/>
            </svg>
        `;
        btnNext.disabled = !hasAnswer;
    }
}

// Расчёт результата
function calculateResult() {
    const answerValues = Object.values(answers);
    const count = {};
    
    answerValues.forEach(val => {
        count[val] = (count[val] || 0) + 1;
    });

    const order = {"А": 0, "Б": 1, "В": 2, "Г": 3};
    const sortedAnswers = Object.entries(count).sort((a, b) => {
        if (b[1] !== a[1]) return b[1] - a[1];
        return order[a[0]] - order[b[0]];
    });

    const maxCount = sortedAnswers[0]?.[1] || 0;
    const dominant = sortedAnswers.filter(([_, v]) => v === maxCount).map(([k]) => k);

    // Если один тип набрал 3+ из 5 — чистый результат
    if (dominant.length === 1 && maxCount >= 3) {
        return RESULTS_MAP[dominant[0]];
    }

    // Смешанные результаты (2 типа)
    const topKeys = sortedAnswers.slice(0, 2).map(([k]) => k).sort((a, b) => order[a] - order[b]);
    if (topKeys.length >= 2) {
        const pair = topKeys.join(',');
        if (MIXED_RESULTS[pair]) {
            return MIXED_RESULTS[pair];
        }
    }

    // Три типа (А + Б + В)
    const topThree = sortedAnswers.slice(0, 3).map(([k]) => k);
    if (topThree.length >= 3) {
        const threeSet = new Set(topThree);
        if (threeSet.has('А') && threeSet.has('Б') && threeSet.has('В')) {
            return THREE_TYPE_RESULT;
        }
    }

    // Фолбэк — показываем результат самого частого ответа
    const mostCommonKey = sortedAnswers[0]?.[0] || 'А';
    return RESULTS_MAP[mostCommonKey] || "Твой результат готов. Начни с маленького шага к себе.";
}

// Показ результата
function showResult() {
    const result = calculateResult();
    
    // Определяем иконку по результату
    if (result.includes('ДЕВОЧКА') && !result.includes('+')) {
        resultIcon.textContent = '👶';
    } else if (result.includes('ПОДРОСТОК') && !result.includes('+')) {
        resultIcon.textContent = '🌟';
    } else if (result.includes('ЖЕНЩИНА-МАТЬ') && !result.includes('+')) {
        resultIcon.textContent = '👩';
    } else if (result.includes('СТАРИЦА') && !result.includes('+')) {
        resultIcon.textContent = '🦉';
    } else {
        resultIcon.textContent = '🎭';
    }

    // Преобразуем \n в <br> для корректного отображения
    resultContent.innerHTML = result.replace(/\n\n/g, '<br><br>').replace(/\n/g, '<br>');
    
    // Скрываем вопросы и навигацию
    questionCard.style.display = 'none';
    navigation.style.display = 'none';
    progressContainer.style.display = 'none';
    
    // Показываем результат
    resultCard.style.display = 'block';
    
    // Скролл наверх
    window.scrollTo({ top: 0, behavior: 'smooth' });
}

